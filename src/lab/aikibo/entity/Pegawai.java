package lab.aikibo.entity;

public class Pegawai {
	
	private String nip;
	private String nmPegawai;
	private String nipBaru;
	
	public String getNip() {
		return nip;
	}
	
	public void setNip(String nip) {
		this.nip = nip;
	}

	public String getNmPegawai() {
		return nmPegawai;
	}

	public void setNmPegawai(String nmPegawai) {
		this.nmPegawai = nmPegawai;
	}

	public String getNipBaru() {
		return nipBaru;
	}

	public void setNipBaru(String nipBaru) {
		this.nipBaru = nipBaru;
	}

}